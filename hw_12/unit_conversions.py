### 1. Function Design for Unit Conversions: 

#### Miles to Kilos: 
def miles_to_kilos(x):
    return (x*1.60934)
    
#### Kilos to Miles: 

def kilos_to_miles(y):
    return(y*.621371) 

### Farenheit to Celsius: 

def far_to_cel(x):
    return((5/9) * (x-32))

### Celsius to Farenheit: 

def cal_to_far(y):
    return(y * (9/5) + 32)

### Kilo to Pounds: 

def kilo_to_lbs(x):
    return (x*2.205)

### Pounds to Kilo: 

def lbs_to_kilo(y):
    return (y * 0.45359237)

        